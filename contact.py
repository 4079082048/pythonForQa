class Contact:

    def __init__(self, username, password, name, firstname, lastname, company, address, mobile, email, bday, bmonth, byear):
        self.username = username
        self.password = password
        self.name = name
        self.firstname = firstname
        self.lastname = lastname
        self.company = company
        self.address = address
        self.mobile = mobile
        self.email = email
        self.bday = bday
        self.bmonth = bmonth
        self.byear = byear